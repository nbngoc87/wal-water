loadpackage <- function(x) {
  if (!suppressWarnings(require(x,character.only = TRUE))) list(install.packages(x, dep=TRUE), library(x, character.only = TRUE))
}




lvls <- function(x) {
  levels(droplevels(as.factor(x)))
}

nlvls <- function(x) {
  nlevels(droplevels(as.factor(x)))
}

